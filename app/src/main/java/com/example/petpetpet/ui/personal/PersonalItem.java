package com.example.petpetpet.ui.personal;

public class PersonalItem {
    private int ImageId;
    private String Title;

    public PersonalItem(){}

    public int getImageId() {
        return ImageId;
    }

    public void setImageId(int imageId) {
        ImageId = imageId;
    }

    public String getTitle() {
        return Title;
    }

    public void setTitle(String title) {
        Title = title;
    }
}
